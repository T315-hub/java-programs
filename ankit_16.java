public class ankit_16 {
    public static void main(String[] args) {
        // swapping of two number not using third number
        int num1 =8;
        int num2=3;
        System.out.println("num1 before swapping : " +num1);
        System.out.println("num1 before swapping : " +num2);
        num1=num1+num2;
        num2=num1-num2;
        num1=num1-num2;
        System.out.println("num1 after swapping : " +num1);
        System.out.println("num1 after swapping : " +num2);

    }
}

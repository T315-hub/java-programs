public class ankit_38 {
    public static void main(String[] args) {
        int arr[]={3,5,6,8,90};
        // in forward direction
        for(int i=0;i< arr.length;i++)
        {
            System.out.println(arr[i]);
        }
        // in backward direction
        for(int i= arr.length-1;i>0;i--)
        {
            System.out.println(arr[i]);
        }
    }
}
